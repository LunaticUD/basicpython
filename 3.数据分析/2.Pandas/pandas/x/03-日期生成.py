import pandas as pd

df = pd.read_excel('03.xlsx',sheet_name=0)
print(df)
# 日期索引 date_range
# df.index = pd.date_range("20250125",periods=4)
df.index = pd.date_range("20250125",periods=len(df))
print(df)
df.index = pd.date_range("20250125",periods=4,freq='7D')
print(df)
df.index = pd.date_range("20250125",periods=4,freq='1M')
print(df)
df.index = pd.date_range("20250125",periods=4,freq='1Y')
print(df)
df.index = pd.date_range("20250125",periods=4,freq='1H')
print(df)
# 日期索引 period_range
df.index = pd.period_range("2025-1",periods=4,freq='M')
print(df)
df.index = pd.period_range("20250101",periods=4,freq='M')
print(df)
df.index = pd.period_range("2025-06-01",freq='W',periods=4)
print(df)
df.index = pd.period_range("2025-06-01",freq='3D',periods=4)
print(df)
df.index = pd.period_range("2025-06-01 11:00:00",freq='30min',periods=4)
print(df)
df.index = pd.period_range("2025-06-01 11:00:00",freq='30s',periods=4)
print(df)
# 工作日 pd.offsets.
df.index = pd.date_range("20250125",periods=4,freq=pd.offsets.MonthBegin(1))
print(df)
df.index = pd.date_range("20250625",periods=4,freq=pd.offsets.BusinessDay(1))
print(df)
df.index = pd.date_range("20250125",periods=4,freq=pd.offsets.BusinessMonthBegin(1))
print(df)
df.index = pd.date_range("20250125",periods=4,freq=pd.offsets.MonthEnd(1))
print(df)
df.index = pd.date_range("20251025",periods=4,freq=pd.offsets.BusinessMonthEnd(1))
print(df)
df.index = pd.date_range("20251025",periods=4,freq=pd.offsets.QuarterBegin(1))
print(df)
# 日期区间 interval_range
df.index = pd.interval_range(start=pd.Timestamp('2025-01-25'),end=pd.Timestamp('2025-01-28'),periods=4)
print(df)